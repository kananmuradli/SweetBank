    @Override
    public void getEdits() {
        this.setLocationRelativeTo(null);
        loginPage.setFocusable(true);
        idText.setText(ID_TEXT_ORIGINAL);
        PasswordText.setText(PASSWORD_TEXT_ORIGINAL);
        TextSettings.setOnlyNumber(idText);
    }

    @Override
    public boolean validInformation() {
        return !(this.idText.getText().equals(this.ID_TEXT_ORIGINAL)
                || String.valueOf(this.PasswordText.getPassword()).equals(this.PASSWORD_TEXT_ORIGINAL));
    }

    @Override
    public AccountInformation getAccountInformation() {
        return AccountInformation.getInstance();
    }

    public UserLogin getUserLoginObject() {
        if(this.userLoginObject == null){
            userLoginObject = new UserLogin();
        }
        return userLoginObject;
    }
    
    
