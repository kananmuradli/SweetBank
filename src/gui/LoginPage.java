    private void btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn1ActionPerformed
        if(this.validInformation()){
            String customerId = this.idText.getText().trim();
            String password = String.valueOf(this.PasswordText.getPassword());
            this.login(customerId, password);
        } else {
            Dialogs.notEmptyMessage(this);
        }
    }//GEN-LAST:event_btn1ActionPerformed

    private void login(String customerId, String password){
        
        this.getUserLoginObject().setCustomerId(customerId);
        this.getUserLoginObject().setPassword(password);
        
        if(getUserLoginObject().loginInformationIsTrue()){
            ActionSettings.setVisible(this, new AccountPage());
        } else {
            Dialogs.showSpecialMessage(this, "Login information is not true!\n"
                    + "Please check your information!!");
        }
    }
    
