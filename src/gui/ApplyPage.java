    private void makesTheApply() {

        this.getUserApplyObject().setNameSurname(this.adSoyadText.getText().trim());
        this.getUserApplyObject().setCustomerNo(this.customerNoText.getText());
        this.getUserApplyObject().setPhoneNo(this.phoneNoText.getText());

        this.getUserApplyObject().setSecurityQuestion(String.valueOf(this.securityQuestion.getSelectedItem()));
        this.getUserApplyObject().setSecurityAnswer(this.securityAnswerText.getText().trim());

        this.getUserApplyObject().setMusteriNo(this.randomMusteriNo());
        this.getUserApplyObject().setPassword(this.randomPassword());

        if (this.getUserApplyObject().applyApproval()) {
            Dialogs.showSpecialMessage(this, "Your apply has been accepted...\n"
                    + "Musteri No:" + this.getUserApplyObject().getMusteriNo()
                    + "\n≈ûifreniz:" +this.getUserApplyObject().getPassword());
            ActionSettings.setVisible(this, new LoginPage());
        } else {
            Dialogs.showSpecialMessage(this, "Your apply has not been accepted. \n"
                    + "Please check your information!!!");

        }

    }

    private String randomMusteriNo() {
        String musteriNo;
        do{
        musteriNo = String.valueOf(1000000 + (int) Math.random() * 9000000);
        }while(this.getUserApplyObject().onlyOneMusteriNo());
        return musteriNo;
    }

    private String randomPassword() {
        String password;
        password = String.valueOf(1000 + (int) Math.random() * 9000);
        return password;
    }

